
//Q.1: Write a program which declares and initializes all 8 primitive datatypes and then uses them in anyway?

public class Q1_PrimitiveDataTypes {

	public static void main(String[] args) {
		
		byte _b = 28;
		short _s = -32768;		
		int _i = 2017;
		long _l = 999999999;
		float _f = 3.14f;
		double _d = 9999999999.99999999999;
		boolean _boolean = true;
		char _c = 'Z';
		
		System.out.println("Value of primitive data type : byte : "+ _b);
		System.out.println("Value of primitive data type : short : "+ _s);
		System.out.println("Value of primitive data type : int : "+ _i);
		System.out.println("Value of primitive data type : long : "+ _l);
		System.out.println("Value of primitive data type : float : "+ _f);
		System.out.println("Value of primitive data type : double : "+ _d);
		System.out.println("Value of primitive data type : Boolean default value : "+ _boolean);
		System.out.println("Value of primitive data type : char : "+ _c);
		
		
		
		

	}

}
